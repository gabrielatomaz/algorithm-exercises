# Crie um script que peça um mês e um ano do usuário 
# e depois apresente um calendário do mês/ano pedido. 
# Use o comando cal para lhe ajudar.

echo Digite o número de um mês:
read month

echo Digite o número de um ano:
read year

echo $(cal $month $year)

